
function array_range(s,e,step){
//-------------------------------------------------------
//值域數值陣列
//-------------------------------------------------------
//s     起始值
//e     終止值
//step  遞增數,預設1,可以指定負整數
//-------------------------------------------------------

    if(!step){
        step=1;
    }else{
        step=parseInt(step);
    }

    var arry=[];
    while(s<=e){
        arry.push(s);
        s=s+step;
    }

    return arry;
}